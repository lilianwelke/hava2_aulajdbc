    private void loadComboBox() {
        try {
            DefaultComboBoxModel model1 = new DefaultComboBoxModel(new Vector(livroDAO.findAll()));
            DefaultComboBoxModel model2 = new DefaultComboBoxModel(new Vector(estudanteDAO.findAll()));
            cbLivro.setModel(model1);
            cbEstudante.setModel(model2);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void loadTabela() {
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        model.setNumRows(0);

        try {
            SimpleDateFormat dataFormatada = new SimpleDateFormat("dd/MM/yyyy");
            for (Emprestimo emprestimo : emprestimoDAO.findAll()) {
                String linha[] = {
                    "" + emprestimo.getEmprestimo_id(), "" + emprestimo.getLivro(), 
                    "" + emprestimo.getEstudante(), 
                    dataFormatada.format(emprestimo.getData_retirada()), 
                    dataFormatada.format(emprestimo.getData_devolucao()), 
                    dataFormatada.format(emprestimo.getData_entrega()), 
                    emprestimo.getStatus()                    
                };
                model.addRow(linha);
            }
        } catch (Exception ex) {
            Logger.getLogger(EmprestimoForm.class.getName()).log(Level.SEVERE, null, ex);
            ex.printStackTrace();
        }
    }

