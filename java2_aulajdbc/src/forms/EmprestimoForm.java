    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        Emprestimo emprestimo = new Emprestimo();
        try {
            emprestimo.setEmprestimo_id(Integer.parseInt(id_emprestimo.getText()));
        } catch (Exception ex) {
            emprestimo.setEmprestimo_id(0);
        }       
        emprestimo.setLivro((Livro) cbLivro.getSelectedItem());
        emprestimo.setEstudante((Estudante) cbEstudante.getSelectedItem());        
        SimpleDateFormat dataFormatada = new SimpleDateFormat("dd/MM/yyyy");
        try {
            emprestimo.setData_retirada(dataFormatada.parse(dataRetirada.getText()));
            emprestimo.setData_devolucao(dataFormatada.parse(dataDevolucao.getText()));
            emprestimo.setData_entrega(dataFormatada.parse(dataEntrega.getText()));
        } catch (ParseException ex) {            
            return;
        }        
        emprestimo.setStatus(status.getText());
        try {
            if (mode.equals("INS")) {
                emprestimoDAO.save(emprestimo);
            } else if (mode.equals("UPD")) {
                emprestimoDAO.update(emprestimo);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            ex.printStackTrace();
        }
        limparCampos();
        loadTabela();
    }//GEN-LAST:event_salvarActionPerformed

    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        String id = tabela.getValueAt(tabela.getSelectedRow(), 0).toString();
        int emprestimo_id = Integer.parseInt(id);
        try {
            Emprestimo emprestimo = emprestimoDAO.findById(emprestimo_id);
            id_emprestimo.setText("" + emprestimo_id);            
            dataRetirada.setText("" + tabela.getValueAt(tabela.getSelectedRow(), 4));            
            dataDevolucao.setText("" + tabela.getValueAt(tabela.getSelectedRow(), 5));
            dataEntrega.setText("" + tabela.getValueAt(tabela.getSelectedRow(), 6));
            status.setText(emprestimo.getStatus());
            cbLivro.getModel().setSelectedItem(emprestimo.getLivro());
            cbEstudante.getModel().setSelectedItem(emprestimo.getEstudante());            
        } catch (Exception ex) {
            Logger.getLogger(EmprestimoForm.class.getName()).log(Level.SEVERE, null, ex);
            ex.printStackTrace();
        }
        this.mode = "UPD";
    }//GEN-LAST:event_tabelaMouseClicked

    private void removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerActionPerformed
        int opt = JOptionPane.showConfirmDialog(this, "Excluir Registro?");
        if (opt == JOptionPane.YES_OPTION) {
            Emprestimo emprestimo = new Emprestimo();
            emprestimo.setEmprestimo_id(Integer.parseInt(tabela.getValueAt(tabela.getSelectedRow(), 0).toString()));
            try {
                emprestimoDAO.delete(emprestimo);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
                ex.printStackTrace();
            }
            limparCampos();
            loadTabela();
        }
    }//GEN-LAST:event_removerActionPerformed

